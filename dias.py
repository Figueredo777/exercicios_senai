dia = '10'
while dia != '0':
    dia = input("Digite um número: ")

    if dia == '1':
        print('domingo')

    elif dia == "2":
        print("Segunda - Feira")

    elif dia == "3":
        print("Terça - Feira")

    elif dia == "4":
        print("Quarta - Feira")

    elif dia == "5":
        print("Quinta - Feira")

    elif dia == "6":
        print("Sexta - Feira")
        
    elif dia == "7":
        print("Sabado")

    else:
        print("Número errado")

print('Fim do programa')
